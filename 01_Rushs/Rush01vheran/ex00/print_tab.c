#include <unistd.h>

void	vh_print_tab(int tab[4][4])
{
	char	n;
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 3)
		{
			n = tab[i][j++] + '0';
			write(1, &n, 1);
			write(1, " ", 1);
		}
		if (j == 3)
		{
			n = tab[i][j] + '0';
			write(1, &n, 1);
			write(1, "\n", 1);
		}
		i++;
	}
}
