int	ft_sqrt(int nb)
{
	int	sq;

	if (nb < 0)
		return (0);
	sq = 1;
	while ((sq * sq) < nb && sq < (nb / 2))
		sq++;
	if (sq * sq == nb)
		return (sq);
	return (0);
}
